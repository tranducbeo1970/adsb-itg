    private void binding() {
        routeConfig = XmlSerializer.load("res/routes.xml", RoutesConfig.class);
        routeConfig.loadDefendency();
        
        tableModel.setNumRows(0);
        
        
        for (int i = 0; i < routeConfig.getLines().size(); i++) {
            RouteLine obj = routeConfig.getLines().get(i);
            //final String listPoint = obj.getPoints().stream().map(Object::toString).collect(Collectors.joining(","));
            this.tableModel.addRow(new Object[]{(i + 1), obj.getName(), obj.getPoints().stream().map(Object::toString).collect(Collectors.joining(", "))});
        }
        
    }

    private void setColumnWidth(JTable table, String name, int min, int max, int prefer) {
        table.getColumn(name).setMinWidth(min);
        table.getColumn(name).setMaxWidth(max);
        table.getColumn(name).setPreferredWidth(prefer);
    }

