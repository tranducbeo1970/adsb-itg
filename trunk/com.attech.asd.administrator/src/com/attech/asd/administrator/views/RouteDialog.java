    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        if (tblPoints.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(rootPane, "You must select one report", "Form validation", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        final int result = JOptionPane.showConfirmDialog(rootPane, "Are you sure ?");
        if (result != JOptionPane.YES_OPTION) {
            return;
        }
        int index = tblPoints.getSelectedRow();
        routeConfig.getLines().remove(index);
        XmlSerializer.serialize("res/routes.xml", routeConfig);
        binding();
    }//GEN-LAST:event_btnRemoveActionPerformed

