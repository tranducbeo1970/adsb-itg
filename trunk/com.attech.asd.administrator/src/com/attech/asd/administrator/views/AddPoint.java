    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
        if (!validated()) {
            return;
        }
        if (index < 0){
            GroundStation obj = new GroundStation();
            obj.setName(txtName.getText().trim().toUpperCase());
            obj.setEnabled(true);
            obj.setX(Float.parseFloat(txtLongitude.getText().trim()));
            obj.setY(Float.parseFloat(txtLatitude.getText().trim()));
            obj.setColor(color[cmbType.getSelectedIndex()]);
            obj.setSize(size[cmbType.getSelectedIndex()]);
            obj.setWeight(1);
            obj.setzIndex(100.0f);
            Label label = new Label(pointConfig.getGroundStations().get(0).getLabel());
            label.setContent(obj.getName());
            obj.setLabel(label);
            pointConfig.getGroundStations().add(obj);
        } else {
            pointConfig.getGroundStations().get(index).setName(txtName.getText().trim().toUpperCase());
            pointConfig.getGroundStations().get(index).setX(Float.parseFloat(txtLongitude.getText().trim()));
            pointConfig.getGroundStations().get(index).setY(Float.parseFloat(txtLatitude.getText().trim()));
            pointConfig.getGroundStations().get(index).getLabel().setContent(txtName.getText().trim().toUpperCase());
        }
        switch (cmbType.getSelectedItem().toString()){
            case "VOR/DME":
                XmlSerializer.serialize("res/vor-dme.xml", pointConfig);
                break;
            case "NDB":
                XmlSerializer.serialize("res/ndb.xml", pointConfig);
                break;
            case "FIXED-POINT":
                XmlSerializer.serialize("res/fix-point.xml", pointConfig);
                break;
        }
        
        JOptionPane.showMessageDialog(rootPane, "Successfully!");
        this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        } finally {
            
        }
    }//GEN-LAST:event_btnSaveActionPerformed

