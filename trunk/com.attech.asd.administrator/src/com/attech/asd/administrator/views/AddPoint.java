    private void binding() {
        switch (cmbType.getSelectedItem().toString()){
            case "VOR/DME":
                pointConfig = XmlSerializer.load("res/vor-dme.xml", GroundStationConfig.class);
                break;
            case "NDB":
                pointConfig = XmlSerializer.load("res/ndb.xml", GroundStationConfig.class);
                break;
            case "FIXED-POINT":
                pointConfig = XmlSerializer.load("res/fix-point.xml", GroundStationConfig.class);
                break;
        }
        
        if (index >= 0) {
            this.txtLatitude.setText(String.format("%s", pointConfig.getGroundStations().get(index).getY()));
            this.txtLongitude.setText(String.format("%s", pointConfig.getGroundStations().get(index).getX()));
            this.txtName.setText(String.format("%s", pointConfig.getGroundStations().get(index).getName()));
        } else {
            this.txtName.setText(String.format(""));
            this.txtLatitude.setText(String.format(""));
            this.txtLongitude.setText(String.format(""));
        }
    }

    private boolean validated() {
        if (!Validator.isEmpty(String.format(txtName.getText()), 1)) {
            JOptionPane.showMessageDialog(rootPane, String.format("Check input value!"));
            txtName.requestFocus();
            return false;
        }
        if (!Validator.isNumberic(String.format(txtLatitude.getText()))) {
            JOptionPane.showMessageDialog(rootPane, String.format("Check input value!"));
            txtLatitude.requestFocus();
            return false;
        }
        if (!Validator.isNumberic(String.format(txtLongitude.getText()))) {
            JOptionPane.showMessageDialog(rootPane, String.format("Check input value!"));
            txtLongitude.requestFocus();
            return false;
        }
        float lat = Float.parseFloat(txtLatitude.getText().trim());
        if (lat > 90f || lat < -90f) {
            txtLatitude.requestFocus();
            return false;
        }
        float lon = Float.parseFloat(txtLongitude.getText().trim());
        if (lon > 180f || lon < -180f) {
            txtLongitude.requestFocus();
            return false;
        }
        
            //validate name unique
            
        for (int i = 0; i < pointConfig.getGroundStations().size(); i++){
            if (i == index)
                continue;
            if (pointConfig.getGroundStations().get(i).getName().equals(txtName.getText().trim().toUpperCase())){
                JOptionPane.showMessageDialog(rootPane, String.format("Name exist. Try another!"));
                txtName.requestFocus();
                return false;
            }
        }
        return true;
    }

