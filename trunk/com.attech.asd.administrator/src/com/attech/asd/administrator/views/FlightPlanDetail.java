    private void binding(int id) {
        if (id > 0) {
            this.plan = new FlightPlanDao().getById(id);
            this.txtType.setText(plan.getCraft());
            this.txtReg.setText(plan.getReg());
            this.txtCallsign.setText(plan.getCallSign());
            this.txtDof.setText(plan.getDof());
            this.txtDep.setText(plan.getDep());
            this.txtDest.setText(plan.getDest());
            this.txtEtd.setText(plan.getEtd());
            this.txtEta.setText(plan.getEta());
            this.txtEet.setText(plan.getEet());
            this.txtAta.setText(plan.getAta());
            this.txtAtd.setText(plan.getAtd());
            this.txtSeq.setText(Objects.toString(plan.getSeq(), ""));
            this.txtRoute.setText(plan.getRoute());
            // SET TOOLTIP
            String tmp = Objects.toString(new AdapterObject().getCraftByReg(txtReg.getText().replaceAll("VN", "VN-")), "");
            this.txtReg.setToolTipText(tmp);
            this.txtType.setToolTipText(tmp);
            String dep = Objects.toString(new AdapterObject().getAirportByIcao(txtDep.getText()), "");
            this.txtDep.setToolTipText(dep);
            String dest = Objects.toString(new AdapterObject().getAirportByIcao(txtDest.getText()), "");
            this.txtDest.setToolTipText(dest);
            this.txtCallsign.disable();
            this.txtDof.disable();
        } else {
            this.txtType.setText(String.format(""));
            this.txtReg.setText(String.format(""));
            this.txtCallsign.setText(String.format(""));
            this.txtDof.setText(String.format(""));
            this.txtDep.setText(String.format(""));
            this.txtDest.setText(String.format(""));
            this.txtEtd.setText(String.format(""));
            this.txtEta.setText(String.format(""));
            this.txtEet.setText(String.format(""));
            this.txtAta.setText(String.format(""));
            this.txtAtd.setText(String.format(""));
            this.txtSeq.setText(String.format(""));
            this.txtRoute.setText(String.format(""));
            // SET TOOLTIP DEFAULT
            this.txtReg.setToolTipText("");
            this.txtType.setToolTipText("");
            this.txtDep.setToolTipText("");
            this.txtDest.setToolTipText("");  
            this.btnDelete.setVisible(false);
        }
    }


    private boolean validated() {
        if(txtSeq.getText().equals("")) return true;
        if (!Validator.isInteger(String.format(txtSeq.getText()), true)) {
            txtSeq.requestFocus();
            return false;
        }
        return true;
    }

