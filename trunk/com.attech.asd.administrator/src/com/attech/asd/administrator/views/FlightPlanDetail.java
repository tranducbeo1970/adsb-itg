    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
        if (!validated()) {
            JOptionPane.showMessageDialog(rootPane, String.format("Check input value!"));
            return;
        }
        if (plan == null) {
            plan = new Flightplan();
        }
        
        plan.setCallSign(txtCallsign.getText().trim());
        plan.setCraft(txtType.getText().trim());
        plan.setReg(txtReg.getText());
        plan.setDof(txtDof.getText());
        plan.setDep(txtDep.getText());
        plan.setDest(txtDest.getText());
        plan.setEtd(txtEtd.getText());
        plan.setEta(txtEta.getText());
        plan.setEet(txtEet.getText());
        plan.setAtd(txtAtd.getText());
        plan.setAta(txtAta.getText());
        if(txtSeq.getText().equals("")){
            plan.setSeq(null);
        }else{
            plan.setSeq(Integer.parseInt(txtSeq.getText()));
        }        
        plan.setRoute(txtRoute.getText().trim());
        plan.save();
        
        JOptionPane.showMessageDialog(rootPane, "Successfully!");
        this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        } finally {
            
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        final int result = JOptionPane.showConfirmDialog(rootPane, "Are you sure ?");
        if (result != JOptionPane.YES_OPTION) {
            return;
        }
        plan.remove();
        this.dispose();
    }//GEN-LAST:event_btnDeleteActionPerformed

