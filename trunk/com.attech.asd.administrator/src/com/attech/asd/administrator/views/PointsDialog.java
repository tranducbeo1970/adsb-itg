    private void tblPointsMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPointsMousePressed
        if (evt.getButton() != 1) {
            return;
        }
        JTable table = (JTable) evt.getSource();
        Point p = evt.getPoint();
        int index = table.rowAtPoint(p);
        if (evt.getClickCount() != 2) {
            return;
        }
        AddPoint dialog = new AddPoint(null, true, cmbType.getSelectedIndex(), index);
        dialog.setVisible(true);
        binding();
    }//GEN-LAST:event_tblPointsMousePressed

