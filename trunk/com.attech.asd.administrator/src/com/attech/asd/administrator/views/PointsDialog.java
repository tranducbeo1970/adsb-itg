    private void binding() {
        switch (cmbType.getSelectedItem().toString()) {
            case "VOR/DME":
                pointConfig = XmlSerializer.load("res/vor-dme.xml", GroundStationConfig.class);
                break;
            case "NDB":
                pointConfig = XmlSerializer.load("res/ndb.xml", GroundStationConfig.class);
                break;
            case "FIXED-POINT":
                pointConfig = XmlSerializer.load("res/fix-point.xml", GroundStationConfig.class);
                break;
        }
        tableModel.setNumRows(0);
        for (int i = 0; i < pointConfig.getGroundStations().size(); i++) {
            this.tableModel.addRow(new Object[]{(i + 1), pointConfig.getGroundStations().get(i).getName(), pointConfig.getGroundStations().get(i).getX(), pointConfig.getGroundStations().get(i).getY()});
        }
    }

    private void setColumnWidth(JTable table, String name, int min, int max, int prefer) {
        table.getColumn(name).setMinWidth(min);
        table.getColumn(name).setMaxWidth(max);
        table.getColumn(name).setPreferredWidth(prefer);
    }

