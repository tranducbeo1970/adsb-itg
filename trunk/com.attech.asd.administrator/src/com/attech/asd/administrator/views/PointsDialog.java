    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        if (tblPoints.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(rootPane, "You must select one report", "Form validation", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        final int result = JOptionPane.showConfirmDialog(rootPane, "Are you sure ?");
        if (result != JOptionPane.YES_OPTION) {
            return;
        }
        int index = tblPoints.getSelectedRow();
        pointConfig.getGroundStations().remove(index);
        switch (cmbType.getSelectedItem().toString()){
            case "VOR/DME":
                XmlSerializer.serialize("res/vor-dme.xml", pointConfig);
                break;
            case "NDB":
                XmlSerializer.serialize("res/ndb.xml", pointConfig);
                break;
            case "FIXED-POINT":
                XmlSerializer.serialize("res/fix-point.xml", pointConfig);
                break;
        }
        binding();
    }//GEN-LAST:event_btnRemoveActionPerformed

