    private void bind(Target target) {
        this.txtCallsign.setText(target.getCallSign());
        this.txtLevel.setText(Float.toString(target.getFlightLevel()));
        this.txtLongtitude.setText(Double.toString(target.getLongtitude()));
        this.txtLatitude.setText(Double.toString(target.getLatitude()));
        this.txtHeading.setText(Double.toString(target.getHeading()));
        this.txtSpeed.setText(Double.toString(target.getSpeed()));
        this.txtNIC.setText(Integer.toString(target.getNic()));
        this.txtNAC.setText(Integer.toString(target.getNac()));
        this.txtSIL.setText(Integer.toString(target.getSil()));
        this.txtStatus.setText(Integer.toString(target.getTargetStatus()));
    }
    
