        @Override
    public void updateCoordinate(Point2f point) {
        bind(point);
    }

    @Override
    public void captureCoordinate(Point2f point) {
        bind(point);
        MouseContext.instance().setMode(MouseMode.Normal);
        btnMouseCapture.setSelected(false);
    }

