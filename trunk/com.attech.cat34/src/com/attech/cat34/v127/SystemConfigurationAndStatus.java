    public static int extract(byte [] bytes, int index, SystemConfigurationAndStatus sysConfigStatus) {
        if (!Byter.validateIndex(index, bytes.length, 1)) return -1;
        int count = 1;
        byte b = bytes[index++];
        boolean sub1 = Byter.extract(b, 7) > 0;
        boolean sub4 = Byter.extract(b, 4) > 0;
        boolean sub5 = Byter.extract(b, 3) > 0;
        boolean sub6 = Byter.extract(b, 2) > 0;
        boolean extend = Byter.extract(b, 0) > 0;
        if (extend) {
            while (extend) {
                if (!Byter.validateIndex(index, bytes.length, 1)) return -1;
                b = bytes[index++];
                extend = Byter.extract(b, 0) > 0;
                count++;
            }
        }
        
        if (sub1) {
            if (!Byter.validateIndex(index, bytes.length, 1)) return -1;
            count ++;
            b = bytes[index++];
            sysConfigStatus.setSubfield1(sub1);
            sysConfigStatus.setSub1Nogo(Byter.extract(b, 7));
            sysConfigStatus.setSub1Rdpc(Byter.extract(b, 6));
            sysConfigStatus.setSub1Rdpr(Byter.extract(b, 5));
            sysConfigStatus.setSub1OvlRdp(Byter.extract(b, 4));
            sysConfigStatus.setSub1OvlXmt(Byter.extract(b, 3));
            sysConfigStatus.setSub1Msc(Byter.extract(b, 2));
            sysConfigStatus.setSub1Tsv(Byter.extract(b, 1));
        }
        
        if (sub4) {
            if (!Byter.validateIndex(index, bytes.length, 1)) return -1;
            count ++;
            b = bytes[index++];
            sysConfigStatus.setSubfield4(sub4);
            sysConfigStatus.setSub4Ant(Byter.extract(b, 7));
            sysConfigStatus.setSub4Chab(Byter.extract(b, 5, 2));
            sysConfigStatus.setSub4Ovl(Byter.extract(b, 4));
            sysConfigStatus.setSub4Msc(Byter.extract(b, 3));
        }
        
        if (sub5) {
            if (!Byter.validateIndex(index, bytes.length, 1)) return -1;
            count ++;
            b = bytes[index++];
            sysConfigStatus.setSubfield5(sub5);
            sysConfigStatus.setSub5Ant(Byter.extract(b, 7));
            sysConfigStatus.setSub5Chab(Byter.extract(b, 5, 2));
            sysConfigStatus.setSub5Ovl(Byter.extract(b, 4));
            sysConfigStatus.setSub5Msc(Byter.extract(b, 3));
        }
        
        if (sub6) {
            if (!Byter.validateIndex(index, bytes.length, 2)) return -1;
            count ++;
            b = bytes[index++];
            sysConfigStatus.setSubfield5(sub5);
            sysConfigStatus.setSub6Ant(Byter.extract(b, 7));
            sysConfigStatus.setSub6Chab(Byter.extract(b, 5, 2));
            sysConfigStatus.setSub6OvlSur(Byter.extract(b, 4));
            sysConfigStatus.setSub6Msc(Byter.extract(b, 3));
            sysConfigStatus.setSub6Scf(Byter.extract(b, 2));
            sysConfigStatus.setSub6Dlf(Byter.extract(b, 1));
            sysConfigStatus.setSub6OvlScf(Byter.extract(b, 0));
            
            count ++;
            b = bytes[index++];
            sysConfigStatus.setSub6OvlDlf(Byter.extract(b, 7));
        }
        
        return count;
    }
    
