package com.attech.asterix.cat21.entities;

/**
 *
 * @author andh
 */
public class GroundVector {
     private float value;
    private float trackAngle;

    /**
     * @return the value
     */
    public float getValue() {
        return value;
    }

    /**
     * @param value the value to set
     */
    public void setValue(float value) {
        this.value = value;
    }

    /**
     * @return the trackAngle
     */
    public float getTrackAngle() {
        return trackAngle;
    }

    /**
     * @param trackAngle the trackAngle to set
     */
    public void setTrackAngle(float trackAngle) {
        this.trackAngle = trackAngle;
    }
    
}
