public class RateOfTurn {
    private short turningIndicator;
    private float rate;

    /**
     * @return the turningIndicator
     */
    public short getTurningIndicator() {
        return turningIndicator;
    }

    /**
     * @param turningIndicator the turningIndicator to set
     */
    public void setTurningIndicator(short turningIndicator) {
        this.turningIndicator = turningIndicator;
    }

    /**
     * @return the rate
     */
    public float getRate() {
        return rate;
    }

    /**
     * @param rate the rate to set
     */
    public void setRate(float rate) {
        this.rate = rate;
    }
}
